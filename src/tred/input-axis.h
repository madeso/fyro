#pragma once

/** Sign type
 */
enum class Sign {
  INVALID  /// Invalid sign
  ,
  POSITIVE  /// Positive sign
  ,
  NEGATIVE  /// Negative sign
  ,
  FULL_RANGE  /// Both positive and negative sign
};


/** Axis types.
 */
enum class Axis {
  INVALID  /// Invalid axis
  ,
  UNBOUND  /// Unbound axis
  ,
  X  /// Mouse X
  ,
  Y  /// Mouse Y
};

